#Three types of operators in Python are:
#1. Arithmetic Operators:addition, subtraction, multiplication, division, modulus, exponentiation
#2. Comparison Operators:equal to, not equal to, greater than, less than, greater than or equal to, less than or equal to
#3. Logical Operators:and, or, not

#for arithmetic operators
print(20 + 5) # Addition
print(20 - 5) # Subtraction
print(20 * 5) # Multiplication
print(20 / 5) # Division
print(20 % 5) # Modulus
print(45 ** 2) # Exponentiation

print("\n") # New line

#for comparison operators
print(20 == 5) # Equal to
print(20 != 5) # Not equal to
print(20 > 5) # Greater than
print(20 < 5) # Less than
print(20 >= 5) # Greater than or equal to
print(20 <= 5) # Less than or equal to  

print("\n") # New line

#for logical operators
is_raining = True
is_cold = False
print(is_raining or is_cold) # Logical OR
print(is_raining and is_cold) # Logical AND
print(not is_raining) # Logical NOT